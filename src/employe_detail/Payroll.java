package employe_detail;

public class Payroll {
	
	private  int emp_id;
	private float basicSalary;
	private float insentive;
	private float benifit;
	public int getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(int emp_id) {
		this.emp_id = emp_id;
	}
	public float getBasicSalary() {
		return basicSalary;
	}
	public void setBasicSalary(float basicSalary) {
		this.basicSalary = basicSalary;
	}
	public float getInsentive() {
		return insentive;
	}
	public void setInsentive(float insentive) {
		this.insentive = insentive;
	}
	public float getBenifit() {
		return benifit;
	}
	public void setBenifit(float benifit) {
		this.benifit = benifit;
	}
	
	
	

}
